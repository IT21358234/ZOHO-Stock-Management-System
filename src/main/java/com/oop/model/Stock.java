package com.oop.model;

public class Stock {
	String S_ID;		
	String S_Name;
	String S_Category;
	int S_Quantity;
	int Purchasing_Price;
	String Supplier_Name;
	String Purchasing_date;
	public String getS_ID() {
		return S_ID;
	}
	public void setS_ID(String s_ID) {
		S_ID = s_ID;
	}
	public String getS_Name() {
		return S_Name;
	}
	public void setS_Name(String s_Name) {
		S_Name = s_Name;
	}
	public String getS_Category() {
		return S_Category;
	}
	public void setS_Category(String s_Category) {
		S_Category = s_Category;
	}
	public int getS_Quantity() {
		return S_Quantity;
	}
	public void setS_Quantity(int s_Quantity) {
		S_Quantity = s_Quantity;
	}
	public int getPurchasing_Price() {
		return Purchasing_Price;
	}
	public void setPurchasing_Price(int purchasing_Price) {
		Purchasing_Price = purchasing_Price;
	}
	public String getSupplier_Name() {
		return Supplier_Name;
	}
	public void setSupplier_Name(String supplier_Name) {
		Supplier_Name = supplier_Name;
	}
	public String getPurchasing_date() {
		return Purchasing_date;
	}
	public void setPurchasing_date(String purchasing_date) {
		Purchasing_date = purchasing_date;
	}
	
	

	
}
