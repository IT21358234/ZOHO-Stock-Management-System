package com.oop.model;

public class Package {
 
	String P_ID;
	String P_Type;
	String P_Height;
	String P_Weight;
	String P_Lenght;
	String Label_Color;
	
	public String getP_ID() {
		return P_ID;
	}
	public void setP_ID(String p_ID) {
		P_ID = p_ID;
	}
	public String getP_Type() {
		return P_Type;
	}
	public void setP_Type(String p_Type) {
		P_Type = p_Type;
	}
	public String getP_Height() {
		return P_Height;
	}
	public void setP_Height(String p_Height) {
		P_Height = p_Height;
	}
	public String getP_Weight() {
		return P_Weight;
	}
	public void setP_Weight(String p_Weight) {
		P_Weight = p_Weight;
	}
	public String getP_Lenght() {
		return P_Lenght;
	}
	public void setP_Lenght(String p_Lenght) {
		P_Lenght = p_Lenght;
	}
	public String getLabel_Color() {
		return Label_Color;
	}
	public void setLabel_Color(String label_Color) {
		Label_Color = label_Color;
	}
  
}
